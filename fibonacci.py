def fibonacci_sequence(n):
    F0, F1 = 0, 1  # กำหนด F0 และ F1 เป็น 0 และ 1 ตามลำดับ
    print(F0)      # แสดงผลค่า F0 ที่เป็น 0 (F(0))
    print(F1)      # แสดงผลค่า F1 ที่เป็น 1 (F(1))
    
    i = 0           # กำหนดตัวแปร i เป็น 2 เพื่อนับตำแหน่ง Fibonacci ตั้งแต่ F(2) เป็นต้นไป
    while i < n:    # วนลูป while จนกว่า i จะน้อยกว่า n (ในที่นี้คือ 10)
        Fi = F1 + F0  # คำนวณค่า Fibonacci ที่ต้องการ Fi = F1 + F0 (F(i) = F(i-1) + F(i-2))
        print(Fi)    # แสดงผลลัพธ์ของค่า Fibonacci ที่คำนวณได้
        F0 = F1      # อัพเดทค่า F0 เป็น F1 สำหรับการคำนวณรอบถัดไป
        F1 = Fi      # อัพเดทค่า F1 เป็น Fi สำหรับการคำนวณรอบถัดไป
        i += 1       # เพิ่มค่า i ขึ้นไป 1 เพื่อคำนวณ Fibonacci ต่อไป

fibonacci_sequence(15)  # เรียกใช้ฟังก์ชันเพื่อหาและแสดง Fibonacci 10 ค่าแรก
